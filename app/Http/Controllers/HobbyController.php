    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request,[
            'hobby' => 'required'
        ]);

        $check = Hobby::where('user_id', auth()->user()->id)->get();
        if($check !== null){
            Hobby::where('user_id', auth()->user()->id)->delete();
        }

        for($i=0; $i<count($request->input('hobby')); $i++){
            $hobby = new Hobby();
            $hobby->user_id = auth()->user()->id;
            $hobby->hobby = $request->input('hobby')[$i];
            $hobby->save();
        }

        return json_encode(['message' => "Hobbies added"], 201);
    }

