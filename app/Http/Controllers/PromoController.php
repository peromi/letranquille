    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'coupon' => 'required|unique:promos',
            'duration' => 'required'
        ]);


        $promo = new Promo();

        $promo->coupon = $request->coupon;
        $promo->days =  $request->duration;

        if ($promo->save()){
            return redirect()->back()->with(['success' => "Code added successfully."]);
        }
    }

